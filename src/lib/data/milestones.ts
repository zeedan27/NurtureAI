export function getMilestonesByAge(months: number): Milestone[] {
  return milestones.filter(
    (m) => months >= m.ageMonthsMin && months <= m.ageMonthsMax
  );
}

export function getMilestonesByCategory(
  category: Milestone['category']
): Milestone[] {
  return milestones.filter((m) => m.category === category);
}

export function getFlaggedMilestones(months: number): Milestone[] {
  return milestones.filter(
    (m) => m.flag && months >= m.ageMonthsMin && months <= m.ageMonthsMax
  );
}

export function getCategoryLabel(category: Milestone['category']): { bn: string; en: string } {
  const labels: Record<Milestone['category'], { bn: string; en: string }> = {
    motor: { bn: 'শারীরিক বিকাশ', en: 'Motor Development' },
    social: { bn: 'সামাজিক বিকাশ', en: 'Social Development' },
    cognitive: { bn: 'জ্ঞানীয় বিকাশ', en: 'Cognitive Development' },
    speech: { bn: 'ভাষা বিকাশ', en: 'Speech Development' },
  };
  return labels[category];
}
