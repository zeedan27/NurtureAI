export function searchMyths(query: string): MythFact[] {
  const lowerQuery = query.toLowerCase();
  return myths.filter(
    (m) =>
      m.myth.toLowerCase().includes(lowerQuery) ||
      m.mythEn.toLowerCase().includes(lowerQuery) ||
      m.fact.toLowerCase().includes(lowerQuery) ||
      m.factEn.toLowerCase().includes(lowerQuery) ||
      m.category.includes(lowerQuery)
  );
}

export function getMythsByCategory(category: string): MythFact[] {
  return myths.filter((m) => m.category === category);
}
